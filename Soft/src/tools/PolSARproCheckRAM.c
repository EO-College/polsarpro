int main(void)
{

  MemoryAlloc = -1; MemoryAlloc = CheckFreeMemory();
  MemoryAlloc = my_max(MemoryAlloc,1000);

  PSP_Threads = omp_get_max_threads();
  if (PSP_Threads <= 2) {
    PSP_Threads = 1;
    } else {
	PSP_Threads = PSP_Threads - 1;
	}
  omp_set_num_threads(PSP_Threads);

  
  MemoryAlloc = -1; MemoryAlloc = CheckFreeMemory();
  printf("MemoryAlloc1 = %i\n",MemoryAlloc);
  MemoryAlloc = my_max(MemoryAlloc,1000);
  printf("MemoryAlloc2 = %i\n",MemoryAlloc);

  PSP_Threads = omp_get_max_threads();
  printf("PSP_Threads1 = %i\n",PSP_Threads);
  if (PSP_Threads <= 2) {
    PSP_Threads = 1;
    } else {
	PSP_Threads = PSP_Threads - 1;
	}
  omp_set_num_threads(PSP_Threads);
  printf("PSP_Threads2 = %i\n",PSP_Threads);
  
  return 0; 
  
}
